package com.proyecto.tm_proyecto;

public class AsistenciasClass {
    String nombre_curso;
    String puntualidad;
    String fecha_asistencia;

    public String getNombre_curso() {
        return nombre_curso;
    }

    public void setNombre_curso(String nombre_curso) {
        this.nombre_curso = nombre_curso;
    }

    public String getPuntualidad() {
        return puntualidad;
    }

    public void setPuntualidad(String puntualidad) {
        this.puntualidad = puntualidad;
    }

    public String getFecha_asistencia() {
        return fecha_asistencia;
    }

    public void setFecha_asistencia(String fecha_asistencia) {
        this.fecha_asistencia = fecha_asistencia;
    }
}
